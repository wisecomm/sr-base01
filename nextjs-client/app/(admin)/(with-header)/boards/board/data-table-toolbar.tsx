"use client";

import * as React from "react";
import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import { Plus, Pencil, Trash2, Search, Calendar } from "lucide-react";
import {
    Select,
    SelectContent,
    SelectItem,
    SelectTrigger,
    SelectValue,
} from "@/components/ui/select";

interface DataTableToolbarProps {
    onAdd: () => void;
    onEdit: () => void;
    onDelete: () => void;
    onSearch: (params: {
        brdId: string;
        searchType: string;
        keyword: string;
        startDate: string;
        endDate: string
    }) => void;
    isLoading?: boolean;
    initialStartDate?: string;
    initialEndDate?: string;
}

export function DataTableToolbar({
    onAdd,
    onEdit,
    onDelete,
    onSearch,
    isLoading,
    initialStartDate = "",
    initialEndDate = ""
}: DataTableToolbarProps) {
    // Search states
    const [searchType, setSearchType] = React.useState("title");
    const [keyword, setKeyword] = React.useState("");
    const [startDate, setStartDate] = React.useState(initialStartDate);
    const [endDate, setEndDate] = React.useState(initialEndDate);

    const startDateRef = React.useRef<HTMLInputElement>(null);
    const endDateRef = React.useRef<HTMLInputElement>(null);

    const handleSearch = () => {
        // Validation: brdId is handled by parent page via URL now
        onSearch({ brdId: "", searchType, keyword, startDate, endDate });
    };

    const handleKeyDown = (event: React.KeyboardEvent) => {
        if (event.key === "Enter") {
            handleSearch();
        }
    };

    const handleDateIconClick = (ref: React.RefObject<HTMLInputElement | null>) => {
        if (ref.current) {
            try {
                ref.current.showPicker();
            } catch (error) {
                ref.current.focus();
                ref.current.click();
            }
        }
    };

    return (
        <div className="flex flex-col gap-4">
            <div className="flex items-center justify-between gap-4">
                <div className="flex flex-1 items-center gap-4 flex-wrap">
                    {/* Board Selection (Required per backend) */}
                    {/* Board Selection Removed - Passed via URL */}

                    <div className="flex items-center gap-2">
                        <span className="text-sm font-medium whitespace-nowrap">검색</span>
                        <Select value={searchType} onValueChange={setSearchType}>
                            <SelectTrigger className="w-[100px]">
                                <SelectValue placeholder="유형" />
                            </SelectTrigger>
                            <SelectContent>
                                <SelectItem value="title">제목</SelectItem>
                                <SelectItem value="contents">내용</SelectItem>
                                <SelectItem value="userId">작성자</SelectItem>
                            </SelectContent>
                        </Select>
                        <Input
                            placeholder="검색어 입력"
                            value={keyword}
                            onChange={(e) => setKeyword(e.target.value)}
                            onKeyDown={handleKeyDown}
                            className="w-[200px]"
                        />
                    </div>

                    <div className="flex items-center gap-2">
                        <span className="text-sm font-medium whitespace-nowrap">기간</span>
                        <div className="flex items-center gap-2">
                            <div className="relative w-[130px]">
                                <Input
                                    type="text"
                                    placeholder="YYYY-MM-DD"
                                    value={startDate}
                                    onChange={(e) => setStartDate(e.target.value)}
                                    className="pr-8"
                                />
                                <button
                                    className="absolute right-0 top-0 h-9 w-9 flex items-center justify-center text-muted-foreground hover:text-primary transition-colors"
                                    type="button"
                                    onClick={() => handleDateIconClick(startDateRef)}
                                >
                                    <Calendar className="h-4 w-4" />
                                </button>
                                <input
                                    type="date"
                                    ref={startDateRef}
                                    className="absolute opacity-0 pointer-events-none"
                                    value={startDate}
                                    onChange={(e) => setStartDate(e.target.value)}
                                    style={{ right: 0, bottom: 0, width: 1, height: 1 }}
                                    tabIndex={-1}
                                />
                            </div>
                            <span>~</span>
                            <div className="relative w-[130px]">
                                <Input
                                    type="text"
                                    placeholder="YYYY-MM-DD"
                                    value={endDate}
                                    onChange={(e) => setEndDate(e.target.value)}
                                    className="pr-8"
                                />
                                <button
                                    className="absolute right-0 top-0 h-9 w-9 flex items-center justify-center text-muted-foreground hover:text-primary transition-colors"
                                    type="button"
                                    onClick={() => handleDateIconClick(endDateRef)}
                                >
                                    <Calendar className="h-4 w-4" />
                                </button>
                                <input
                                    type="date"
                                    ref={endDateRef}
                                    className="absolute opacity-0 pointer-events-none"
                                    value={endDate}
                                    onChange={(e) => setEndDate(e.target.value)}
                                    style={{ right: 0, bottom: 0, width: 1, height: 1 }}
                                    tabIndex={-1}
                                />
                            </div>
                        </div>
                    </div>

                    <Button
                        variant="outline"
                        onClick={handleSearch}
                        disabled={isLoading}
                        className="font-bold border-border text-foreground hover:bg-muted"
                    >
                        <Search className="mr-2 h-4 w-4" />
                        조회
                    </Button>
                </div>

                <div className="flex items-center space-x-2">
                    <Button
                        variant="outline"
                        onClick={onAdd}
                        className="font-bold border-border text-foreground hover:bg-muted"
                    >
                        <Plus className="mr-2 h-4 w-4" />
                        추가
                    </Button>

                    <Button
                        variant="outline"
                        onClick={onEdit}
                        className="font-bold border-border text-foreground hover:bg-muted"
                    >
                        <Pencil className="mr-2 h-4 w-4" />
                        수정
                    </Button>

                    <Button
                        variant="outline"
                        onClick={onDelete}
                        className="font-bold border-border text-foreground hover:bg-muted"
                    >
                        <Trash2 className="mr-2 h-4 w-4" />
                        삭제
                    </Button>
                </div>
            </div>
        </div>
    );
}
